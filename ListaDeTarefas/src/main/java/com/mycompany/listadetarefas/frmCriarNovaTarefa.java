    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed

        try {

            Connection conexao = conectarBanco();

            String sql = "INSERT INTO tarefas (tarefa, dataCriacao, dataVencimento, prioridade, statuss, notas) VALUES (?, ?, ?, ?, ?, ?)";

            PreparedStatement pstmt = conexao.prepareStatement(sql);

            String tarefa = txtAddTarefa.getText();
            String prioridade = cmbAddPrioridade.getSelectedItem().toString();
            String status = "Pendente";
            String notas = txtAddNotas.getText();

            LocalDate dataAtual = LocalDate.now();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

            Date dataCriacao = Date.valueOf(dataAtual);
//            Date dataConclusao
            Date dataVencimento = new Date(sdf.parse(txtAddVencimento.getText()).getTime());

            pstmt.setString(1, tarefa);
            pstmt.setDate(2, dataCriacao);
            pstmt.setDate(3, dataVencimento);
            pstmt.setString(4, prioridade);
            pstmt.setString(5, status);
            pstmt.setString(6, notas);

            pstmt.executeUpdate();

            pstmt.close();
            conexao.close();

            JOptionPane.showMessageDialog(this, "Dados salvos com sucesso!");

            abrirLista();
            dispose();

        } catch (SQLException ex) {
            Logger.getLogger(frmCriarNovaTarefa.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(frmCriarNovaTarefa.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnCriarActionPerformed

